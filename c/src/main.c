void rotaryIntHandler()
{
	int pin1 = digitalRead( PIN_ROTARY_1 );
	int pin2 = digitalRead( PIN_ROTARY_2 );
	int res = updateRotary(&rotary_state, pin1, pin2);
	if( res > 0 ) {
		printf( "clockwise\n" );
	} else if( res < 0 ) {
		printf( "counter-clockwise\n" );
	}
}

